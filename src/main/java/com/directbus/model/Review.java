package com.directbus.model;

public class Review {
	
	String text;
	int starNumber;
	
	
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public int getStarNumber() {
		return starNumber;
	}
	public void setStar(int star) {
		this.starNumber = star;
	}
	
	

}
